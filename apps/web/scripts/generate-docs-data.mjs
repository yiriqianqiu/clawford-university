// Build-time script: reads docs markdown files and outputs
// src/lib/docs-data.generated.ts so pages can access
// doc content without filesystem access (required for Cloudflare Workers).
// Usage: node scripts/generate-docs-data.mjs
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DOCS_DIR = path.resolve(__dirname, "../../../content/docs");
const OUTPUT = path.resolve(__dirname, "../src/lib/docs-data.generated.ts");

const DOC_ORDER = [
  "index",
  "skill-format",
  "playbook-format",
  "cli",
  "compatibility",
  "campus",
];

const TITLE_MAP = {
  "index": "Getting Started",
  "skill-format": "Skill Format",
  "playbook-format": "Playbook Format",
  "cli": "CLI Reference",
  "compatibility": "Compatibility",
  "campus": "Campus",
};

function extractTitle(content, slug) {
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1] : TITLE_MAP[slug] ?? slug;
}

function readLocale(locale) {
  const dir = path.join(DOCS_DIR, locale);
  if (!fs.existsSync(dir)) return [];

  return fs
    .readdirSync(dir)
    .filter((f) => f.endsWith(".md"))
    .map((f) => {
      const slug = f.replace(".md", "");
      const content = fs.readFileSync(path.join(dir, f), "utf-8");
      const order = DOC_ORDER.indexOf(slug);
      return {
        locale,
        slug,
        title: extractTitle(content, slug),
        order: order === -1 ? 999 : order,
        content,
      };
    })
    .sort((a, b) => a.order - b.order);
}

const locales = fs
  .readdirSync(DOCS_DIR)
  .filter((d) => fs.statSync(path.join(DOCS_DIR, d)).isDirectory());

const allDocs = locales.flatMap(readLocale);

const code = `// AUTO-GENERATED by scripts/generate-docs-data.mjs — do not edit

export interface DocEntry {
  locale: string;
  slug: string;
  title: string;
  order: number;
  content: string;
}

export const DOCS_DATA: readonly DocEntry[] = ${JSON.stringify(allDocs, null, 2)} as const;
`;

fs.writeFileSync(OUTPUT, code, "utf-8");
console.log(`Generated ${OUTPUT} with ${allDocs.length} doc entries across ${locales.length} locales`);
