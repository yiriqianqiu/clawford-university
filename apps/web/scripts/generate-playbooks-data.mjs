// Build-time script: reads playbook MDX files and outputs
// src/lib/playbooks-data.generated.ts so pages can access
// playbook content without filesystem access (required for Cloudflare Workers).
// Usage: node scripts/generate-playbooks-data.mjs
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PLAYBOOKS_DIR = path.resolve(__dirname, "../../../playbooks");
const OUTPUT = path.resolve(__dirname, "../src/lib/playbooks-data.generated.ts");

function readLocale(locale) {
  const dir = path.join(PLAYBOOKS_DIR, locale);
  if (!fs.existsSync(dir)) return [];

  return fs
    .readdirSync(dir)
    .filter((f) => f.startsWith("playbook-") && f.endsWith(".mdx"))
    .map((f) => {
      const id = f.replace("playbook-", "").replace(".mdx", "");
      const content = fs.readFileSync(path.join(dir, f), "utf-8");
      return { locale, id, content };
    });
}

const locales = fs
  .readdirSync(PLAYBOOKS_DIR)
  .filter((d) => fs.statSync(path.join(PLAYBOOKS_DIR, d)).isDirectory());

const allContent = locales.flatMap(readLocale);

const code = `// AUTO-GENERATED by scripts/generate-playbooks-data.mjs — do not edit

export interface PlaybookContentEntry {
  locale: string;
  id: string;
  content: string;
}

export const PLAYBOOKS_CONTENT: readonly PlaybookContentEntry[] = ${JSON.stringify(allContent, null, 2)} as const;
`;

fs.writeFileSync(OUTPUT, code, "utf-8");
console.log(`Generated ${OUTPUT} with ${allContent.length} playbook content entries across ${locales.length} locales`);
