// Build-time script: reads skills manifests and outputs
// src/lib/skills-data.generated.ts so API routes can access
// skill data without filesystem access (required for Cloudflare Workers).
// Usage: node scripts/generate-skills-data.mjs
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SKILLS_DIR = path.resolve(__dirname, "../../../skills");
const OUTPUT = path.resolve(__dirname, "../src/lib/skills-data.generated.ts");

const dirs = fs.readdirSync(SKILLS_DIR).filter((d) => {
  return fs.existsSync(path.join(SKILLS_DIR, d, "manifest.json"));
});

const skills = dirs.map((dir) => {
  const raw = fs.readFileSync(path.join(SKILLS_DIR, dir, "manifest.json"), "utf-8");
  const m = JSON.parse(raw);
  return {
    slug: dir,
    name: m.name,
    description: m.description,
    category: m.category,
    tags: m.tags ?? [],
    capabilities: m.capabilities ?? [],
    triggers: m.triggers ?? [],
    dependencies: m.dependencies ?? {},
    expectedImprovement: m.expectedImprovement ?? 0,
  };
});

const code = `// AUTO-GENERATED by scripts/generate-skills-data.mjs — do not edit
import type { Skill } from "./skills";

export const SKILLS_DATA: Skill[] = ${JSON.stringify(skills, null, 2)} as const;
`;

fs.writeFileSync(OUTPUT, code, "utf-8");
console.log(`Generated ${OUTPUT} with ${skills.length} skills`);
