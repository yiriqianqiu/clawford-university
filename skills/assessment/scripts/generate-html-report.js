// ─── Radar chart SVG (inline) ───────────────────────────────────────────────

function generateRadarSVG(dimensions) {
  const W = 400, H = 400, CX = 200, CY = 210, R = 140, N = 5;
  const GRIDS = [20, 40, 60, 80, 100];

  const scores = dimensions.map(d => d.status === 'answered' ? d.adjScore : 0);

  function polarToXY(angle, radius) {
    const rad = (angle - 90) * (Math.PI / 180);
    return { x: CX + radius * Math.cos(rad), y: CY + radius * Math.sin(rad) };
  }
  function axisAngle(i) { return (i / N) * 360; }
  function fmt(n) { return n.toFixed(1); }

  // Grid polygons
  const grids = GRIDS.map(pct => {
    const pts = Array.from({ length: N }, (_, i) => {
      const p = polarToXY(axisAngle(i), (pct / 100) * R);
      return `${fmt(p.x)},${fmt(p.y)}`;
    }).join(' ');
    return `<polygon points="${pts}" fill="none" stroke="#E2E8F0" stroke-width="${pct === 100 ? 1.5 : 0.8}" ${pct < 100 ? 'stroke-dasharray="4,4"' : ''}/>`;
  }).join('\n');

  // Grid labels
  const gridLabels = GRIDS.map(pct => {
    const p = polarToXY(0, (pct / 100) * R);
    return `<text x="${fmt(p.x + 4)}" y="${fmt(p.y - 2)}" font-size="9" fill="#94A3B8" font-family="system-ui,sans-serif">${pct}</text>`;
  }).join('\n');

  // Axes
  const axes = Array.from({ length: N }, (_, i) => {
    const tip = polarToXY(axisAngle(i), R);
    return `<line x1="${CX}" y1="${CY}" x2="${fmt(tip.x)}" y2="${fmt(tip.y)}" stroke="#CBD5E1" stroke-width="1"/>`;
  }).join('\n');

  // Score polygon
  const scorePts = scores.map((s, i) => {
    const p = polarToXY(axisAngle(i), (s / 100) * R);
    return `${fmt(p.x)},${fmt(p.y)}`;
  }).join(' ');

  // Score dots
  const dots = scores.map((s, i) => {
    const p = polarToXY(axisAngle(i), (s / 100) * R);
    let color = '#EF4444';
    if (s >= 90) color = '#10B981';
    else if (s >= 75) color = '#3B82F6';
    else if (s >= 60) color = '#F59E0B';
    return `<circle cx="${fmt(p.x)}" cy="${fmt(p.y)}" r="5" fill="${color}" stroke="white" stroke-width="2"/>`;
  }).join('\n');

  // Labels
  const labels = dimensions.map((d, i) => {
    const angle = axisAngle(i);
    const lp = polarToXY(angle, R + 28);
    let anchor = 'middle';
    if (angle > 10 && angle < 170) anchor = 'start';
    else if (angle > 190 && angle < 350) anchor = 'end';
    let dy = 0;
    if (angle < 30 || angle > 330) dy = -4;
    else if (angle > 150 && angle < 210) dy = 12;

    const s = scores[i];
    let sColor = '#EF4444';
    if (s >= 90) sColor = '#10B981';
    else if (s >= 75) sColor = '#3B82F6';
    else if (s >= 60) sColor = '#F59E0B';

    const name = isZH ? (DIM_NAMES_ZH[d.id] || d.name) : d.name;
    return `<text x="${fmt(lp.x)}" y="${fmt(lp.y + dy)}" text-anchor="${anchor}" font-size="11" font-weight="600" fill="#1E293B" font-family="system-ui,sans-serif">${d.id} ${name}</text>
<text x="${fmt(lp.x)}" y="${fmt(lp.y + dy + 15)}" text-anchor="${anchor}" font-size="14" font-weight="700" fill="${sColor}" font-family="system-ui,sans-serif">${s.toFixed(1)}</text>`;
  }).join('\n');

  // Overall badge
  const ov = data.overall.adj.toFixed(1);
  const ovLabel = isZH ? '综合' : 'OVERALL';

  return `<svg width="${W}" height="${H}" viewBox="0 0 ${W} ${H}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${W}" height="${H}" fill="white" rx="12"/>
  ${grids}
  ${gridLabels}
  ${axes}
  <circle cx="${CX}" cy="${CY}" r="3" fill="#94A3B8" opacity="0.4"/>
  <polygon points="${scorePts}" fill="#3B82F6" fill-opacity="0.15" stroke="#2563EB" stroke-width="2" stroke-linejoin="round"/>
  ${dots}
  ${labels}
  <rect x="${CX - 32}" y="${CY - 16}" width="64" height="32" rx="8" fill="white" stroke="#E2E8F0" stroke-width="1.5"/>
  <text x="${CX}" y="${CY - 2}" text-anchor="middle" font-size="8" fill="#64748B" font-family="system-ui,sans-serif">${ovLabel}</text>
  <text x="${CX}" y="${CY + 11}" text-anchor="middle" font-size="14" font-weight="700" fill="#0F172A" font-family="system-ui,sans-serif">${ov}</text>
</svg>`;
}

