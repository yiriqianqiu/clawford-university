// ─── Question detail cards ──────────────────────────────────────────────────

function generateQuestionCards(dimensions) {
  return dimensions.map((d, idx) => {
    const name = isZH ? (DIM_NAMES_ZH[d.id] || d.name) : d.name;
    const diff = isZH ? (DIFF_ZH[d.difficulty] || d.difficulty) : d.difficulty;
    const statusText = d.status === 'answered' ? t.answered : t.skipped;

    if (d.status === 'skipped') {
      return `
      <div class="question-card skipped">
        <div class="q-header">
          <span class="q-num">Q${idx + 1}</span>
          <span class="q-dim">${d.id} ${name}</span>
          <span class="q-status skip-badge">${statusText}</span>
        </div>
        <p class="skip-reason">${isZH ? '原因' : 'Reason'}: ${d.skipReason || 'Required capability not available'}</p>
      </div>`;
    }

    const criteriaRows = (d.criteria || []).map(c => `
      <tr>
        <td>${c.name}</td>
        <td>${(c.weight * 100).toFixed(0)}%</td>
        <td><span class="score-pill ${c.score >= 4 ? 'high' : c.score >= 3 ? 'mid' : 'low'}">${c.score}/5</span></td>
        <td class="justification">${c.justification || ''}</td>
      </tr>`).join('');

    const confLabel = isZH
      ? { high: '高', medium: '中', low: '低' }[d.confidence] || d.confidence
      : d.confidence;

    return `
    <div class="question-card">
      <div class="q-header">
        <span class="q-num">Q${idx + 1}</span>
        <span class="q-dim">${d.id} ${name}</span>
        <span class="q-diff">${diff} ×${d.multiplier}</span>
        <span class="q-score-badge" style="background:${d.adjScore >= 80 ? '#DCFCE7' : d.adjScore >= 60 ? '#FEF3C7' : '#FEE2E2'};color:${d.adjScore >= 80 ? '#166534' : d.adjScore >= 60 ? '#92400E' : '#991B1B'}">${d.adjScore.toFixed(1)}</span>
        <span class="q-status">${statusText}</span>
      </div>

      <details class="q-details">
        <summary>${t.question}</summary>
        <div class="q-text">${escapeHtml(d.question || '')}</div>
      </details>

      <details class="q-details" open>
        <summary>${t.answer} (${t.confidence}: ${confLabel})</summary>
        <div class="q-answer">${escapeHtml(d.answer || '')}</div>
      </details>

      <details class="q-details">
        <summary>${t.scoring} — ${t.raw}: ${d.rawScore} → ${t.adjusted}: ${d.adjScore.toFixed(1)}</summary>
        <table class="criteria-table">
          <thead><tr><th>${t.criterion}</th><th>${t.weight}</th><th>${t.score}</th><th>${t.justification}</th></tr></thead>
          <tbody>${criteriaRows}</tbody>
        </table>
      </details>
    </div>`;
  }).join('\n');
}

function escapeHtml(str) {
  return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
    .replace(/\n/g, '<br>');
}

// ─── Level display ──────────────────────────────────────────────────────────

function levelDisplay(level) {
  const color = LEVEL_COLORS[level] || '#64748B';
  const label = isZH ? (LEVEL_ZH[level] || level) : level;
  return `<span class="level-badge" style="background:${color}">${label}</span>`;
}

